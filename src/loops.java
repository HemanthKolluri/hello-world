public class loops {
    public static void main(String[] args) {
        int i=0;
        while (i<5){
            System.out.println("hello World "+i);
            i++;
        }

        for(int j=0;j<10;j++){
            System.out.println(j);
        }

        int z=10;
        if(z==10){
            System.out.println("z is 10");
        }else{
            System.out.println("z is not 10");
        }
    }
}
